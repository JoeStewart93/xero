def build_feature_doc(f: dict) -> str:
    fid = f"F{f['id']}"
    deps = ", ".join(f["depends"]) if f["depends"] else "—"
    lines = [
        f"# {fid}: {f['title']}",
        "",
        "## Metadata",
        "| Field | Value |",
        "|---|---|",
        f"| ID | {fid} |",
        f"| Priority | {f['priority']} |",
        "| Status | Planned |",
        f"| MVP Phase | {f['phase']} |",
        f"| Depends on | {deps} |",
        "",
        "## Summary",
        f["summary"],
        "",
        "## Requirements",
    ]
    for r in f["requirements"]:
        lines.append(f"- {r}")
    lines.extend(["", "## Stages", ""])
    for i, (name, goal, ac) in enumerate(f["stages"], 1):
        lines.extend([
            f"### Stage {i}: {name}",
            f"**Goal:** {goal}",
            "**Acceptance Criteria:**",
        ])
        for item in ac:
            lines.append(f"- [ ] {item}")
        lines.append("")
    lines.extend(["## Feature Acceptance Criteria", ""])
    for item in f["feature_ac"]:
        lines.append(f"- [ ] {item}")
    lines.extend([
        "",
        "## Test Plan",
        "",
        "### Unit Tests",
    ])
    for item in f["unit"]:
        lines.append(f"- [ ] {item}")
    lines.extend(["", "### System / Integration Tests"])
    for item in f["integration"]:
        lines.append(f"- [ ] {item}")
    lines.extend(["", "### Playwright Tests"])
    for item in f["playwright"]:
        lines.append(f"- [ ] {item}")
    lines.append("")
    return "\n".join(lines)


def main() -> None:
    out = Path(__file__).resolve().parent.parent / "spec" / "features"
    out.mkdir(parents=True, exist_ok=True)
    for f in FEATURES:
        path = out / f"{f['id']}-{f['slug']}.md"
        path.write_text(build_feature_doc(f), encoding="utf-8")
        print(f"Wrote {path.name}")
    print(f"Generated {len(FEATURES)} feature specs in {out}")


if __name__ == "__main__":
    main()
